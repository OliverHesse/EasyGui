package net.lucent.easygui.properties;

/**
 * for example x positioning start = left end = right center = center
 * y positioning start = top end = bottom  center = center
 * can then be used for sticky because how it is handled depends on the users
 * initial choice of how the item is positioned relative to the current container
 *
 */
public enum Positioning {
    START,
    END,
    CENTER
}
